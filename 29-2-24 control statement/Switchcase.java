 class SwitchcaseVowel {

    public static void main(String[] args) {
        int num = 5;
        switch (num) {
            case a:
                System.out.println("this is Vowel");
                break;
            case e:
                System.out.println("this is Vowel");
                break;
            case i:
                System.out.println("this is Vowel");
                break;
            case o:
                System.out.println("this is Vowel");
                break;
            case u:
                System.out.println("this is Vowel");
                break;
            case 5:
                System.out.println("this is Vowel");
                break;
            default:
                System.out.println("this is not Vowel");
                break;
        }
    }
}
