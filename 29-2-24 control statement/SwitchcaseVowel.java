 class SwitchcaseVowel {

    public static void main(String[] args) {
        String z = "x";
        switch (z) {
            case "a":
                System.out.println("this is Vowel");
                break;
            case "e":
                System.out.println("this is Vowel");
                break;
            case "i":
                System.out.println("this is Vowel");
                break;
            case "o":
                System.out.println("this is Vowel");
                break;
            case "u":
                System.out.println("this is Vowel");
                break;
            default:
                System.out.println( z + " this is not Vowel");
                break;
        }
    }
}
